function subtraction (a, b) {
    return a - b;
}
console.log("result of 5-3 = " + subtraction(5, 3));


function division (a, b) {
    return a / b;
}
console.log("result of 10/2 = " + division(10, 2));


function multiplication (a, b) {
    return a * b;
}
console.log("result of 4*6 = " + multiplication(4, 6));


function  percentage (num, per) {
    return (num/100)*per;
}
console.log("result of 38% of 87 = " + percentage(87, 38));


function distanceHour(_km, _h) {
    return _km / _h;
}
console.log("average speed of 150km over 2 hours = " + distanceHour(150, 2) + "km/h");